package com.ifaru.measurement;

public class PhysicalSize {
  
  private MeasurementUnit standingHeightMU;
  private MeasurementUnit standingWidthMU;
  private MeasurementUnit standingDepthMU;
  private float standingHeight;
  private float standingWidth;
  private float standingDepth;
  
  public MeasurementUnit getStandingHeightMU() {
    return standingHeightMU;
  }
  public void setStandingHeightMU(MeasurementUnit standingHeightMU) {
    this.standingHeightMU = standingHeightMU;
  }
  public MeasurementUnit getStandingWidthMU() {
    return standingWidthMU;
  }
  public void setStandingWidthMU(MeasurementUnit standingWidthMU) {
    this.standingWidthMU = standingWidthMU;
  }
  public MeasurementUnit getStandingDepthMU() {
    return standingDepthMU;
  }
  public void setStandingDepthMU(MeasurementUnit standingDepthMU) {
    this.standingDepthMU = standingDepthMU;
  }
  public float getStandingHeight() {
    return standingHeight;
  }
  public void setStandingHeight(float standingHeight) {
    this.standingHeight = standingHeight;
  }
  public float getStandingWidth() {
    return standingWidth;
  }
  public void setStandingWidth(float standingWidth) {
    this.standingWidth = standingWidth;
  }
  public float getStandingDepth() {
    return standingDepth;
  }
  public void setStandingDepth(float standingDepth) {
    this.standingDepth = standingDepth;
  }
  
  
  

}
