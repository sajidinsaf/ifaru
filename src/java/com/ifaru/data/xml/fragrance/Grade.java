package com.ifaru.data.xml.fragrance;

public class Grade {

  private String id, name, rateQuantity, rateUnit, currency;
  private double rate;
  /**
   * @return the currency
   */
  public String getCurrency() {
    return currency;
  }
  /**
   * @param currency the currency to set
   */
  public void setCurrency(String currency) {
    this.currency = currency;
  }
  /**
   * @return the id
   */
  public String getId() {
    return id;
  }
  /**
   * @param id the id to set
   */
  public void setId(String id) {
    this.id = id;
  }
  /**
   * @return the name
   */
  public String getName() {
    return name;
  }
  /**
   * @param name the name to set
   */
  public void setName(String name) {
    this.name = name;
  }
  /**
   * @return the quantity
   */
  public String getRateQuantity() {
    return rateQuantity;
  }
  /**
   * @param quantity the quantity to set
   */
  public void setRateQuantity(String quantity) {
    this.rateQuantity = quantity;
  }
  /**
   * @return the rate
   */
  public double getRate() {
    return rate;
  }
  /**
   * @param rate the rate to set
   */
  public void setRate(double rate) {
    this.rate = rate;
  }
  /**
   * @return the rateUnit
   */
  public String getRateUnit() {
    return rateUnit;
  }
  /**
   * @param rateUnit the rateUnit to set
   */
  public void setRateUnit(String rateUnit) {
    this.rateUnit = rateUnit;
  }
  
  
  
}
